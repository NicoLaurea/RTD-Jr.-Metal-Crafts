     It started with its first service which is creating rings made of copper, silver and gold. Every details of each rings are created by hand. As the market in metal crafting expands, the business soon ride the trend. Adding services in creating personalized pins, medals, trophies, plaques, markers and etc. Customer or target market are mostly government agencies or entities, such as militaries and major offices.
     Rogelio reached his aged of working into resting, is peacefully in rest on January 2021. Heirs took over the business, from sole proprietorship turned into corporation making all the sons and daughters the shareholders of the corporation. Making Rodrigo Dumlao as the current CEO and COO. The named was changed into RTD Jr. Metal Crafts, making its market expanded from different craftsmanship.</p>
